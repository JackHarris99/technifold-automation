/**
 * Admin Login Page - Dev-minimum authentication
 * Checks password against ADMIN_SECRET env var
 */

import { cookies } from 'next/headers';
import { redirect } from 'next/navigation';

export default function AdminLoginPage() {
  async function handleLogin(formData: FormData) {
    'use server';

    const password = formData.get('password') as string;
    const adminSecret = process.env.ADMIN_SECRET;

    if (!adminSecret) {
      throw new Error('ADMIN_SECRET not configured');
    }

    if (password === adminSecret) {
      const cookieStore = await cookies();
      cookieStore.set('admin_authorized', 'true', {
        httpOnly: true,
        secure: process.env.NODE_ENV === 'production',
        sameSite: 'lax',
        maxAge: 60 * 60 * 24 * 7, // 7 days
      });
      redirect('/admin');
    } else {
      redirect('/admin/login?error=invalid');
    }
  }

  return (
    <div className="min-h-screen flex items-center justify-center bg-gray-50">
      <div className="max-w-md w-full space-y-8 p-8 bg-white rounded-lg shadow">
        <div>
          <h2 className="text-center text-3xl font-extrabold text-gray-900">
            Admin Login
          </h2>
        </div>
        <form className="mt-8 space-y-6" action={handleLogin}>
          <div>
            <label htmlFor="password" className="sr-only">
              Password
            </label>
            <input
              id="password"
              name="password"
              type="password"
              required
              className="appearance-none rounded relative block w-full px-3 py-2 border border-gray-300 placeholder-gray-500 text-gray-900 focus:outline-none focus:ring-blue-500 focus:border-blue-500 focus:z-10 sm:text-sm"
              placeholder="Admin password"
            />
          </div>
          <div>
            <button
              type="submit"
              className="group relative w-full flex justify-center py-2 px-4 border border-transparent text-sm font-medium rounded-md text-white bg-blue-600 hover:bg-blue-700 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-blue-500"
            >
              Sign in
            </button>
          </div>
        </form>
      </div>
    </div>
  );
}
